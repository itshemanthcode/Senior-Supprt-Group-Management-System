    <div class="benefit">
      <h3>Getting Practical Advice and Information</h3>
      <p>Support group members can share advice about treatment, medication, and alternative options. Exchanging information about positive outcomes can empower members of the group to take a more active role in their care or seek out treatment options previously unknown to them.</p>
    </div>

    <div class="benefit">
      <h3>Developing Coping Skills and Managing Expectations</h3>
      <p>For seniors facing a new diagnosis, having the opportunity to speak openly and frankly can be comforting as well as educational. Support group members may include those whose disease and treatment is more advanced. For many, lack of information on what to expect is frightening, and a ‚Äúveteran‚Äù can provide valuable insight into treatment and progression of the disease.</p>
    </div>

